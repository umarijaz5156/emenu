<div id="add_variant">
    <h2>Variants:</h2>
    <p>
        <b><i>Variants allows you to add additional options to an order.</i></b>
        <br/>
        <span style="color:red;font-style: italic">Warning: if you are using variants your customer have to choose one of these variants, otherwise he/she won't be able to make an order. If you need to allow your user to skip choosing, you should use Extras instead. </span>
    </p>
    <div class="text-center">
        <img src = "{{asset('images') }}/docs_19_01_23/variants1.png"/>
    </div>
    <h4>1.How to create a variants</h4>
    <p>
        In order to make this feature work you have to enable it under the <b>"Item settings"</b> in your restourant dashboard. To do that please navigate to an <b>Items menu</b> section and choose desired item:
    </p>
    <div class="text-center">
        <img src = "{{asset('images') }}/docs_19_01_23/variants2.png"/>
    </div>
    <br/>
    <p>
        After you choose desired item please scroll down to bottom, enable variants options and press save.
    </p>
    <div class="text-center mb-1">
        <img src = "{{asset('images') }}/docs_19_01_23/variants3.png"/>
    </div>
    <p>
        After that you will see additional variants block at the top right part of the page. To edit variants  please press <b>Edit Oprions</b> at the variant block.
    </p>
    <div class="text-center mb-1">
        <img src = "{{asset('images') }}/docs_19_01_23/variants4.png"/>
    </div>
    <p>
        Here you can see a list of varians, you can add new variants, or edit/delete already created variants.
    </p>
    <div class="text-center mb-1">
        <img src = "{{asset('images') }}/docs_19_01_23/variants5.png"/>
    </div>
    <h4>2.How to use variants</h4>
    <p>
        Please open a order creation popup under restourants menu front page and choose your options and make an order.
    </p>
    <div class="text-center mb-1">
        <img src = "{{asset('images') }}/docs_19_01_23/variants6.png"/>
    </div>
    <p>
        <span style="color:orange">Note: Price will be recalculated after you choose all variants </span>
    </p>
    <p>
        After you make an order all chosen variants and extras can be seen inside your order invoice.
    </p>
    <div class="text-center mb-1">
        <img src = "{{asset('images') }}/docs_19_01_23/variants7.png"/>
    </div>
</div>

